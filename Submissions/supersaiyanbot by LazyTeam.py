    # MAIN FUNCTION that returns a single move to the game manager
    def get_move(self, player, enemy, player_projectiles, enemy_projectiles):
        distance = abs(get_pos(player)[0] - get_pos(enemy)[0])
        
        # Uses super saiyan off cooldown from (0-20), (40-60), (80-100)
        if not secondary_on_cooldown(player):
            return SECONDARY
        
        # Uses a dash if the dash is in range
        if 2 <= distance <= 5 and primary_on_cooldown(player) is False:
            return PRIMARY

        # Once close enough uses heavy and light
        if distance == 1 and heavy_on_cooldown(player) is True:
                return LIGHT
        if distance == 1 and heavy_on_cooldown(player) is False:
             return HEAVY
        
        #Default move: jump forward to avoid projectiles and other enemy attacks, e.g. bear trap, hadoken
        return JUMP_FORWARD
        
